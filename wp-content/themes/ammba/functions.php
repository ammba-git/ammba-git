function afc_image($image, $size = 'large') {
	$alt = '';

	if (is_array($image)) {
	
		if(isset($image['alt']) && isset($image['title']) ){
			$alt = ($image['alt'] !== '') ? $image['alt'] : $image['title'];
		}

		if (isset($image['sizes']) && isset($image['sizes'][$size])) {
			echo '<img src="'. $image['sizes'][$size] .'" alt="'. $alt .'">';
		}
	}
}

